#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	int		src_len;
	int		i;

	i = 0;
	src_len = ft_strlen(src);
	str = (char *)malloc((src_len + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
